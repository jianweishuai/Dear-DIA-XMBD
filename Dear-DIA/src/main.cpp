void deardia::DecodeMzXMLData(std::vector< std::vector<float> >& spectrum,
	int peaksCount, int precision, std::string& peakString, std::string zlib)
{
	spectrum.resize(2, std::vector<float>(peaksCount));

	unsigned char* uncomp_result;
	size_t size = Base64::textToBinarySize(peakString.length());

	if (zlib == "zlib")
	{
		unsigned char* result = (unsigned char*)malloc(size * sizeof(unsigned char));
		Base64::textToBinary(peakString.c_str(), peakString.length(), result);

		unsigned long retLen = 2 * peaksCount * precision;
		uncomp_result = (unsigned char*)malloc(retLen * sizeof(unsigned char));

		int OK = uncompress(uncomp_result, &retLen, result, size);

		free(result);

		if (OK != 0)
		{
			std::cerr << "Something was wrong in decode spectrum data in mzXML file.\n\n";

			free(uncomp_result);
			exit(0);
		}
	}
	else
	{
		uncomp_result = (unsigned char*)malloc(size * sizeof(unsigned char));
		Base64::textToBinary(peakString.c_str(), peakString.length(), uncomp_result);
	}

	int p = 0;
	for (int i = 0; i < peaksCount; ++i)
	{
		for (int j = 0; j < 2; ++j)
		{
			int beg = p;
			int end = p + precision - 1;
			unsigned char tmp = 0;

			while (beg < end)
			{
				tmp = uncomp_result[beg];
				uncomp_result[beg] = uncomp_result[end];
				uncomp_result[end] = tmp;

				++beg;
				--end;
			}
			p += precision;
		}
	}

	if (precision == 4)
	{
		float* arr = (float*)uncomp_result;
		for (int i = 0; i < peaksCount; ++i)
		{
			spectrum[0][i] = (float)(arr[2 * i]);
			spectrum[1][i] = (float)(arr[2 * i + 1]);
		}
	}
	else
	{
		double* arr = (double*)uncomp_result;
		for (int i = 0; i < peaksCount; ++i)
		{
			spectrum[0][i] = (double)(arr[2 * i]);
			spectrum[1][i] = (double)(arr[2 * i + 1]);
		}
	}
	free(uncomp_result);
}

void deardia::ParallelWorkflow(int nThreads, std::string fileName)
{
	tbb::concurrent_vector<deardia::MgfData> tandemSpectra;

	tbb::parallel_pipeline(nThreads,
		tbb::make_filter<void, deardia::SpectraData>(tbb::filter::serial_out_of_order,
			[&](tbb::flow_control& fc)
			{
				deardia::SpectraData spectraData;
				deardia::sliderQueue.pop(spectraData);

				if (spectraData.stop)
				{
					fc.stop();
				}

				return spectraData;
			}) &

		tbb::make_filter<deardia::SpectraData, void>(tbb::filter::parallel,
			[&](deardia::SpectraData spectraData)
			{
				if (spectraData.ms1Spectra.size() > 0)
				{
					deardia::ProcessSliderData(spectraData, tandemSpectra);

				}
				return NULL;

			})
				);

	std::ofstream MgfFile(deardia::mgfFile);

	if (!MgfFile)
	{
		std::cout << "Cannot open mgf file: " << deardia::mgfFile
			<< "\nPlease cheak your file or directory.\n";
		exit(1);
	}
	MgfFile << std::fixed << std::setprecision(3);

	for (int i = 0; i < tandemSpectra.size(); ++i)
	{
		MgfFile << "BEGIN IONS\n"
			<< "PEPMASS=" << tandemSpectra[i].ms1Mz << "\n"
			<< "RTINSECONDS=" << tandemSpectra[i].retentionTime << "\n"
			<< "CHARGE=" << (int)tandemSpectra[i].ms1Charge << "\n";

		for (int j = 0; j < tandemSpectra[i].hitSpectrum.size(); ++j)
		{
			MgfFile << tandemSpectra[i].hitSpectrum[j][0] << " "
				<< (int)tandemSpectra[i].hitSpectrum[j][1] << "\n";
		}

		MgfFile << "END IONS\n" << std::endl;
	}

	MgfFile.close();
	std::cout << "the number of pesudo spectra: " << tandemSpectra.size() << std::endl;
}

