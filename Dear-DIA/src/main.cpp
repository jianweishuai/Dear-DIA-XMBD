float KMEANS::dist2(Point& x, Point& y)
{
	float sum = 0.0f;

	for (int i = 0; i < N_FEATURES; ++i)
	{
		sum += (x.data[i] - y.data[i]) * (x.data[i] - y.data[i]);
	}

	return sum;
}

void deardia::ClusterAgain(
	int sliderwinId,
	int sliderId,
	float sliderRetentionTime,
	std::vector<int>& usedPpid,
	std::vector<int>& sliderMS2Mz,
	std::vector<float>& sliderMS2Intensity,
	std::vector< std::vector<float> >& sliderMS2XICs,
	std::vector< std::vector<float> >& binSpectra,
	deardia::MgfData& mgfData)
{
	int winId = sliderwinId;
	int nSamples = sliderMS2Mz.size();

	tsl::robin_set<int> skipPpid;
	for (int i = 0; i < usedPpid.size(); ++i)
	{
		skipPpid.insert(usedPpid[i]);
	}

	tsl::robin_map< int, std::vector<int> > ms2MapToPpid;
	for (int i = 0; i < nSamples; ++i)
	{
		ms2MapToPpid[sliderMS2Mz[i]] = std::vector<int>();
	}

	for (int k = 0; k < deardia::windowContainPpid[winId].size(); ++k)
	{
		int ppid = deardia::windowContainPpid[winId][k];

		if (skipPpid.find(ppid) != skipPpid.end()) continue;

		for (int i = 0; i < deardia::ppidMapToPeptideInfo[ppid].ionMzList.size(); ++i)
		{
			int n = deardia::ppidMapToPeptideInfo[ppid].ionMzList.size() / 2;
			int binMz = (int)(deardia::ppidMapToPeptideInfo[ppid].ionMzList[i] * deardia::ms2BinSize);

			if (ms2MapToPpid.find(binMz) != ms2MapToPpid.end())
			{
				if (i < n) // b ions
				{
					ms2MapToPpid[binMz].push_back(-ppid);
				}
				else // y ions
				{
					ms2MapToPpid[binMz].push_back(ppid);
				}
			}
		}
	}

	tsl::robin_map<int, std::vector<int> > ppidHitMS2;

	for (int i = 0; i < sliderMS2Mz.size(); ++i)
	{
		int mz = sliderMS2Mz[i];

		for (int j = 0; j < ms2MapToPpid[mz].size(); ++j)
		{
			int ppid = std::abs(ms2MapToPpid[mz][j]);

			int index = i + 1;
			if (ms2MapToPpid[mz][j] < 0) index = -(i + 1);

			if (ppidHitMS2.find(ppid) == ppidHitMS2.end())
			{
				std::vector<int> tmp = { index };
				ppidHitMS2[ppid] = tmp;
			}
			else
			{
				ppidHitMS2[ppid].push_back(index);
			}
		}

	}

	std::vector< std::pair<int, double> > hyperScoreList;
	for (auto it = ppidHitMS2.begin(); it != ppidHitMS2.end(); ++it)
	{
		int Nb = 0;
		int Ny = 0;
		double sumIntensity = 0.0;

		for (auto it2 = it->second.begin(); it2 != it->second.end(); ++it2)
		{
			int idx = *it2;
			if (idx < 0)
			{
				++Nb;
				idx = -idx;
				sumIntensity += sliderMS2Intensity[idx - 1];
			}
			else
			{
				++Ny;
				sumIntensity += sliderMS2Intensity[idx - 1];
			}
		}
		if (Nb + Ny < deardia::minHitNumber) continue;

		double hyperScore = deardia::Log10Factorial[Nb] + deardia::Log10Factorial[Ny] + log10(sumIntensity);
		hyperScoreList.push_back(std::make_pair(it.key(), hyperScore));
	}

	if (hyperScoreList.size() > 1)
	{
		tbb::parallel_sort(hyperScoreList.begin(), hyperScoreList.end(),
			[](std::pair<int, double>& x, std::pair<int, double>& y)
			{
				return x.second > y.second;
			});
	}

	for (int i = 0; i < hyperScoreList.size(); ++i)
	{
		int ppid = hyperScoreList[i].first;

		if (skipPpid.find(ppid) != skipPpid.end()) continue;
		if (deardia::ppidMapToPeptideInfo[ppid].isDecoy) continue;

		int ms1Charge = -1;
		float ms1Mz = -1.0f;
		bool findMS1 = false;
		float pepMass = deardia::ppidMapToPeptideInfo[ppid].pepMass;

		for (int z = deardia::ms1MinCharge; z <= deardia::ms1MaxCharge; ++z)
		{
			float mz = pepMass / (float)z + 1.007276466879f;

			if (mz >= deardia::winRange[winId][0] &&
				mz <= deardia::winRange[winId][1])
			{
				ms1Mz = mz;
				ms1Charge = z;
				findMS1 = true;
				break;
			}
		}
		if (findMS1 == false) break;

		tsl::robin_map<int, float> theoFragments;
		for (int i = 0; i < deardia::ppidMapToPeptideInfo[ppid].ionMzList.size(); ++i)
		{
			int key = (int)(deardia::ppidMapToPeptideInfo[ppid].ionMzList[i] * deardia::ms2BinSize);
			float value = deardia::ppidMapToPeptideInfo[ppid].ionMzList[i];
			theoFragments[key] = value;
		}

		std::vector< std::pair<int, float> > sortIntensity;
		for (int j = 0; j < ppidHitMS2[ppid].size(); ++j)
		{
			int idx = ppidHitMS2[ppid][j];
			if (idx < 0) idx = -idx;
			idx -= 1;

			if (theoFragments.find(sliderMS2Mz[idx]) != theoFragments.end())
			{
				sortIntensity.push_back(std::make_pair(idx, sliderMS2Intensity[idx]));
			}
		}

		std::sort(sortIntensity.begin(), sortIntensity.end(),
			[](std::pair<int, float>& x, std::pair<int, float>& y)
			{
				return x.second > y.second;
			});

		float targetPearson = 0.0f;
		for (int i = 0; i < 5; ++i)
		{
			for (int j = i + 1; j < 6; ++j)
			{
				int idx1 = sortIntensity[i].first;
				int idx2 = sortIntensity[j].first;
				targetPearson += deardia::PearsonCorrcoef(sliderMS2XICs[idx1],
					sliderMS2XICs[idx2]);
			}
		}
		targetPearson /= 15.0;
		if (targetPearson < 0.95f) break;

		std::vector< std::vector<float> > ms2Spectrum;
		for (int j = 0; j < ppidHitMS2[ppid].size(); ++j)
		{
			int idx = ppidHitMS2[ppid][j];
			if (idx < 0) idx = -idx;
			idx -= 1;

			if (theoFragments.find(sliderMS2Mz[idx]) != theoFragments.end())
			{
				float mz = theoFragments[sliderMS2Mz[idx]];

				std::vector<float> tmp = { mz, sliderMS2Intensity[idx] };
				ms2Spectrum.push_back(tmp);
			}
		}

		tbb::parallel_sort(ms2Spectrum.begin(), ms2Spectrum.end(),
			[](std::vector<float>& x, std::vector<float>& y)
			{
				return x[0] < y[0];
			});

		mgfData.ms1Mz = ms1Mz;
		mgfData.ms1Charge = ms1Charge;
		mgfData.retentionTime = sliderRetentionTime;

		mgfData.hitSpectrum = ms2Spectrum;

		break;
	}
}

int KMEANS::nearest(Point& pt, std::vector<Point>& centers, int n_centers, float* d2)
{
	int min_idx = pt.group;
	float min_dist = FLT_MAX;

	float dist = 0.0f;

	for (int i = 0; i < n_centers; ++i)
	{
		if (min_dist > (dist = KMEANS::dist2(pt, centers[i])))
		{
			min_dist = dist;
			min_idx = i;
		}
	}

	if (d2) *d2 = min_dist;

	return min_idx;
}

