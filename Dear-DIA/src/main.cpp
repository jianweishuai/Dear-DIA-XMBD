void deardia::DecodeMzMLData(std::vector< std::vector<float> >& spectrum,
	int peaksCount, int precision, std::vector<std::string>& peakString, std::string zlib)
{
	spectrum.resize(peakString.size(), std::vector<float>(peaksCount));

	for (int n = 0; n < peakString.size(); ++n)
	{
		unsigned char* uncomp_result;
		size_t size = Base64::textToBinarySize(peakString[n].length());

		if (zlib == "zlib")
		{
			unsigned char* result = (unsigned char*)malloc(size * sizeof(unsigned char));
			Base64::textToBinary(peakString[n].c_str(), peakString[n].length(), result);

			unsigned long retLen = peaksCount * precision;
			uncomp_result = (unsigned char*)malloc(retLen * sizeof(unsigned char));

			int OK = uncompress(uncomp_result, &retLen, result, size);

			(free)(result);

			if (OK != 0)
			{
				std::cerr << "Something was wrong in decode peak data.\n\n";

				std::cerr << peakString[n] << std::endl;

				free(uncomp_result);
				exit(3);
			}
		}
		else
		{
			uncomp_result = (unsigned char*)malloc(size * sizeof(unsigned char));
			Base64::textToBinary(peakString[n].c_str(), peakString[n].length(), uncomp_result);
		}

		if (precision == 4)
		{
			float* arr = (float*)uncomp_result;
			for (int i = 0; i < peaksCount; ++i)
			{
				spectrum[n][i] = arr[i];
			}
		}
		else
		{
			double* arr = (double*)uncomp_result;
			for (int i = 0; i < peaksCount; ++i)
			{
				spectrum[n][i] = (double)(arr[i]);
			}
		}
		(free)(uncomp_result);
	}
}



void KMEANS::kpp(std::vector<Point>& pts, int n_samples, std::vector<Point>& centers, int n_clusters)
{
	centers[0] = pts[0];

	float sum;
	float* d = (float*)malloc(n_samples * sizeof(float));

	for (int i = 1; i < n_clusters; ++i)
	{
		sum = 0.0f;

		for (int j = 0; j < n_samples; ++j)
		{
			KMEANS::nearest(pts[j], centers, i, d + j);
			sum += d[j];
		}
		sum = KMEANS::randf(sum);

		for (int j = 0; j < n_samples; ++j)
		{
			if ((sum -= d[j]) > 0) continue;

			centers[i] = pts[j];

			break;
		}

	}

	tbb::parallel_for(0, n_samples, [&](int j)
		{
			pts[j].group = KMEANS::nearest(pts[j], centers, n_clusters, 0);
		});

	free(d);
}

