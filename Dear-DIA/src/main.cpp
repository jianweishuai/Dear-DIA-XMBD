void SplitProteinAASeq(std::string& protAASeq,
	std::vector<deardia::PeptideInfo>& splitPeptInfo, int ptid, bool isDecoy)
{
	std::vector<int> splitPos;
	splitPos.push_back(-1);

	for (int i = 0; i < protAASeq.length(); ++i)
	{
		if (protAASeq[i] == 'K' || protAASeq[i] == 'R')
		{
			splitPos.push_back(i);
		}
		if (protAASeq[i] == 'P')
		{
			if (!splitPos.empty())
			{
				if (splitPos.back() + 1 == i)
				{
					splitPos.pop_back();
				}
			}
		}
	}

	if (splitPos.back() != protAASeq.length() - 1)
	{
		splitPos.push_back(protAASeq.length() - 1);
	}

	int k = -1;
	std::vector<int> missCleavage;
	std::vector< std::vector<int> > cutPos;
	std::vector< std::vector<int> > prevNextIdx;

	for (int i = 0; i < splitPos.size() - 1; ++i)
	{
		k = -1;
		for (int j = i + 1; j <= i + 1 + deardia::missCleavage; ++j)
		{
			if (j < splitPos.size())
			{
				++k;

				std::vector<int> tmp(2, 0);
				tmp[0] = splitPos[i] + 1;
				tmp[1] = splitPos[j] - tmp[0] + 1;

				if (tmp[1] < deardia::pepMinLength ||
					tmp[1] > deardia::pepMaxLength) continue;

				std::vector<int> tmp1 = { tmp[0] - 1, tmp[0] + tmp[1] };
				prevNextIdx.push_back(tmp1);

				cutPos.push_back(tmp);
				missCleavage.push_back(k);
			}
		}
	}

	std::vector<char> invalidAA = { 'B', 'J', 'O', 'U', 'X', 'Z' };

	for (int i = 0; i < cutPos.size(); ++i)
	{
		bool invalid = false;
		std::string peptSeq = protAASeq.substr(cutPos[i][0], cutPos[i][1]);

		for (int j = 0; j < 6; ++j)
		{
			if (invalid) break;

			for (int k = 0; k < peptSeq.length(); ++k)
			{
				if (peptSeq[k] == invalidAA[j])
				{
					invalid = true;
					break;
				}
			}
		}
		if (invalid) continue;

		std::vector<int> offset = { ptid, cutPos[i][0], cutPos[i][1] };

		deardia::PeptideInfo info;

		info.isDecoy = isDecoy;
		if (i <= deardia::missCleavage)
		{
			info.isNTerm = true;
		}

		info.peptSeq = peptSeq;
		info.missCleavage = missCleavage[i];

		splitPeptInfo.push_back(info);
	}
}

