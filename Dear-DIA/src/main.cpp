void deardia::MS1WindowRangeFromMzML(std::string inputFile)
{
	FILE* xml_file;
	xml_file = fopen(inputFile.c_str(), "r");

	char* buffer_char = (char*)malloc((deardia::xmlBufferSize + 1) * sizeof(char));
	buffer_char[deardia::xmlBufferSize] = 0;

	int pos;
	bool flag = false;

	int cycle_id = -1;
	int middle_id = -1;
	int middle_rt = (int)(deardia::sliderWidth / 2);

	int arrayCount = -1;
	std::string name = "";
	std::string mslevel = "";
	std::string contents = "";
	std::string peak_string = "";

	std::string precursor_mz = "";
	std::string ms1_lowerOffset = "";
	std::string ms1_upperOffset = "";

	tsl::robin_map<std::string, std::vector<float>  > windowInfo;

	std::cout << "\nGet MS1 window ranges from " << inputFile << " ..." << std::endl;

	while (1)
	{
		if (feof(xml_file)) break;
		size_t len = fread(buffer_char, 1, deardia::xmlBufferSize, xml_file);

		for (int i = 0; i < deardia::xmlBufferSize; ++i)
		{
			if (buffer_char[i] < ' ') buffer_char[i] = ' ';

			if (!flag && buffer_char[i] == '<')
			{
				flag = true;
			}
			else if (flag && buffer_char[i] != '<' && buffer_char[i] != '>')
			{
				contents += buffer_char[i];
			}
			else if (!flag && buffer_char[i] != '<' && buffer_char[i] != '>')
			{
				if (name == "binary>")
				{
					peak_string += buffer_char[i];
				}
			}
			else if (flag && buffer_char[i] == '>')
			{
				flag = false;
				contents += buffer_char[i];

				pos = contents.find(' ');
				name = contents.substr(0, pos);

				if (name == "cvParam")
				{
					if (mslevel.length() == 0)
					{
						if (contents.find("MS:1000511") != contents.npos) // ms level
						{
							mslevel = deardia::FindMzMLKeyValue(contents, "value=\"");

							if (mslevel == "1")
							{
								++cycle_id;
								++middle_id;
							}
						}
					}
				}

				if (mslevel == "2")
				{
					if (name == "cvParam")
					{
						if (precursor_mz.length() == 0)
						{
							if (contents.find("MS:1000827") != contents.npos) // compression
							{
								std::string paramName = deardia::FindMzMLKeyValue(contents, "name=\"");
								if (paramName != "isolation window target m/z")
								{
									std::cout << "Cannot find \"isolation window target m/z\" in \"MS:1000827\"!\n"
										<< "Please check mzML file!";
									exit(0);
								}

								precursor_mz = deardia::FindMzMLKeyValue(contents, "value=\"");
							}
						}
						if (ms1_lowerOffset.length() == 0)
						{
							if (contents.find("MS:1000828") != contents.npos)
							{
								ms1_lowerOffset = deardia::FindMzMLKeyValue(contents, "value=\"");
							}
						}
						if (ms1_upperOffset.length() == 0)
						{
							if (contents.find("MS:1000829") != contents.npos)
							{
								ms1_upperOffset = deardia::FindMzMLKeyValue(contents, "value=\"");
							}
						}
					}
				}

				if (name == "binaryDataArrayList")
				{
					arrayCount = atoi(deardia::FindMzMLKeyValue(contents, "count=\"").c_str());
				}

				if (arrayCount != 0)
				{
					if (name == "/binary>")
					{
						--arrayCount;

						peak_string = "";
					}

					if (arrayCount == 0)
					{
						if (cycle_id == -1)
						{
							mslevel = "";
							contents = "";
							peak_string = "";
							precursor_mz = "";
							ms1_lowerOffset = "";
							ms1_upperOffset = "";
							continue;
						}

						if (mslevel == "2")
						{
							if (windowInfo.find(precursor_mz) == windowInfo.end())
							{
								float center = std::stof(precursor_mz);
								float lower = std::stof(ms1_lowerOffset);
								float upper = std::stof(ms1_upperOffset);

								std::vector<float> temp = { center - lower, center + upper };

								windowInfo[precursor_mz] = temp;
							}
						}

						mslevel = "";
						peak_string = "";
						precursor_mz = "";

						ms1_lowerOffset = "";
						ms1_upperOffset = "";
					}
				}
				contents = "";
			}
		}
	}

	fclose(xml_file);
	(free)(buffer_char);

	for (auto it = windowInfo.begin(); it != windowInfo.end(); ++it)
	{
		deardia::winRange.push_back(it.value());
	}

	std::sort(deardia::winRange.begin(), deardia::winRange.end(),
		[&](std::vector<float>& x, std::vector<float>& y)
		{
			return x[0] < y[0];
		});

	std::cout << "Set MS1 window ranges done!\n"
		<< "The maximum number of MS1 windows is " << deardia::winRange.size() << std::endl;

	if (deardia::VERBOSE >= 1)
	{
		for (int i = 0; i < deardia::winRange.size(); ++i)
		{
			for (int j = 0; j < 2; ++j)
			{
				std::cout << deardia::winRange[i][j] << ' ';
			}
			std::cout << "\n";
		}
		std::cout << "\n";
	}
}

float deardia::GetDecoyXICsPearson(int ppid, std::vector< std::vector<float> >& binSpectra)
{
	float H2O = 18.010565f;
	float PROTON = 1.007825f;

	std::vector<float> AAMassList = deardia::ppidMapToPeptideInfo[ppid].AAMassList;

	std::reverse(AAMassList.begin(), AAMassList.end());

	float pepMass = -((float)AAMassList.size() - 1.0f) * H2O;
	for (int i = 0; i < AAMassList.size(); ++i)
	{
		pepMass += AAMassList[i];
	}

	std::vector<int> byIonList;
	tsl::robin_set<int> skipIons;

	for (int z = 1; z <= 1; ++z)
	{
		float sumMass = AAMassList[0];

		float yFirstIonMz = (pepMass + z * PROTON) / z;

		if (yFirstIonMz > deardia::ms2MinMz && yFirstIonMz < deardia::ms2MaxMz)
		{
			int iyFirstIonMz = (int)(yFirstIonMz * deardia::ms2BinSize);

			if (skipIons.find(iyFirstIonMz) == skipIons.end())
			{
				skipIons.insert(iyFirstIonMz);
				byIonList.push_back(iyFirstIonMz);
			}
		}

		for (int j = 1; j < AAMassList.size(); ++j)
		{
			float bIonMass = sumMass - H2O + PROTON;
			float yIonMass = pepMass - bIonMass + 2.0f * PROTON;

			float bIonMz = (bIonMass + PROTON * (z - 1)) / z;
			float yIonMz = (yIonMass + PROTON * (z - 1)) / z;

			if (bIonMz > deardia::ms2MinMz && bIonMz < deardia::ms2MaxMz)
			{
				int ibIonMz = (int)(bIonMz * deardia::ms2BinSize);

				if (skipIons.find(ibIonMz) == skipIons.end())
				{
					skipIons.insert(ibIonMz);
					byIonList.push_back(ibIonMz);
				}
			}

			if (yIonMz > deardia::ms2MinMz && yIonMz < deardia::ms2MaxMz)
			{
				int iyIonMz = (int)(yIonMz * deardia::ms2BinSize);

				if (skipIons.find(iyIonMz) == skipIons.end())
				{
					skipIons.insert(iyIonMz);
					byIonList.push_back(iyIonMz);
				}
			}

			sumMass += AAMassList[j] - H2O;
		}

		float bEndIonMz = (sumMass - H2O + z * PROTON) / z;

		if (bEndIonMz > deardia::ms2MinMz && bEndIonMz < deardia::ms2MaxMz)
		{
			int ibEndIonMz = (int)(bEndIonMz * deardia::ms2BinSize);

			if (skipIons.find(ibEndIonMz) == skipIons.end())
			{
				skipIons.insert(ibEndIonMz);
				byIonList.push_back(ibEndIonMz);
			}
		}
	}

	std::vector< std::vector<float> > ms2XICs;
	std::vector< std::pair<int, float> > sortIntensity;

	int idx = 0;
	for (auto it = byIonList.begin(); it != byIonList.end(); ++it)
	{
		int nonZeroCount = 0;
		float maxValue = 0.0f;
		std::vector<float> curve(deardia::sliderWidth, 0.0f);

		for (int t = 0; t < deardia::sliderWidth; ++t)
		{
			if (binSpectra[t].size() == 0) continue;
			if (*it >= binSpectra[t].size()) continue;

			if (binSpectra[t][*it] > 1.0f)
			{
				++nonZeroCount;
				curve[t] = binSpectra[t][*it];

				if (curve[t] > maxValue)
				{
					maxValue = curve[t];
				}
			}
		}

		if (nonZeroCount < 5) continue;

		ms2XICs.push_back(curve);
		sortIntensity.push_back(std::make_pair(idx, maxValue));

		++idx;
	}

	if (sortIntensity.size() < 3)
	{
		return -1.0f;
	}

	if (sortIntensity.size() > 6)
	{
		tbb::parallel_sort(sortIntensity.begin(), sortIntensity.end(),
			[](std::pair<int, float>& x, std::pair<int, float>& y)
			{
				return x.second > y.second;
			});

		float avgP = 0.0f;
		for (int i = 0; i < 5; ++i)
		{
			for (int j = i + 1; j < 6; ++j)
			{
				int idx1 = sortIntensity[i].first;
				int idx2 = sortIntensity[j].first;
				//avgP += deardia::PearsonCorrcoef(ms2XICs[idx1], ms2XICs[idx2]);
				avgP += deardia::ConcordanceCorrcoef(ms2XICs[idx1], ms2XICs[idx2]);
			}
		}
		avgP /= 15.0f;

		return avgP;
	}
	else
	{
		float avgP = 0.0f;
		float count = 0.0f;
		for (int i = 0; i < sortIntensity.size() - 1; ++i)
		{
			for (int j = i + 1; j < sortIntensity.size(); ++j)
			{
				int idx1 = sortIntensity[i].first;
				int idx2 = sortIntensity[j].first;
				//avgP += deardia::PearsonCorrcoef(ms2XICs[idx1], ms2XICs[idx2]);
				avgP += deardia::ConcordanceCorrcoef(ms2XICs[idx1], ms2XICs[idx2]);
				count += 1.0f;
			}
		}
		avgP /= count;

		return avgP;
	}
}




