void deardia::ProcessSliderData(deardia::SpectraData& spectraData,
	tbb::concurrent_vector<deardia::MgfData>& tandemSpectra)
{
	int winId = spectraData.winId;
	int sliderId = spectraData.sliderId;
	int numWindows = deardia::winRange.size();

	std::vector<int64_t> sliderMS1Key;
	std::vector<float> sliderMS1ApexMz;
	std::vector<int> sliderMS1ApexCharge;
	std::vector< std::vector<float> > sliderMS1XICs;

	deardia::ProcessMS1(winId, sliderId, sliderMS1Key, sliderMS1ApexMz,
		sliderMS1ApexCharge, sliderMS1XICs, spectraData.ms1Spectra);

	if (sliderMS1Key.size() > 0)
	{
		tsl::robin_set<int> candidateMS2;
		for (int i = 0; i < sliderMS1Key.size(); ++i)
		{
			int64_t key = sliderMS1Key[i];

			if (deardia::ms1MapToPpid.find(key) == deardia::ms1MapToPpid.end())
			{
				continue;
			}

			uint32_t ppid = 0;

			if (deardia::ms1MapToPpid[key].size() > 0)
			{
				for (int j = 0; j < deardia::ms1MapToPpid[key].size(); ++j)
				{
					ppid = deardia::ms1MapToPpid[key][j];

					for (int k = 0; k < deardia::ppidMapToPeptideInfo[ppid].ionMzList.size(); ++k)
					{
						int binMz = (int)(deardia::ppidMapToPeptideInfo[ppid].ionMzList[k] * deardia::ms2BinSize);
						candidateMS2.insert(binMz);
					}
				}
			}
		}

		if (candidateMS2.size() > 0)
		{
			std::vector< std::vector<float> > binSpectra(deardia::sliderWidth);

			for (int t = 0; t < deardia::sliderWidth; ++t)
			{
				if (spectraData.ms2Spectra[t].spectrum.size() == 0) continue;

				int maxSize = (int)((spectraData.ms2Spectra[t].spectrum[0].back() + 10.0f) * deardia::ms2BinSize);

				binSpectra[t].resize(maxSize, 0.0f);

				for (int i = 0; i < spectraData.ms2Spectra[t].spectrum[0].size(); ++i)
				{
					float mz = spectraData.ms2Spectra[t].spectrum[0][i];
					float intensity = spectraData.ms2Spectra[t].spectrum[1][i];

					if (mz > deardia::winRange[winId][0] &&
						mz < deardia::winRange[winId][1])
					{
						continue;
					}

					if (mz < deardia::ms2MinMz ||
						mz > deardia::ms2MaxMz ||
						intensity < 1.0f)
					{
						continue;
					}

					int iMz = (int)(mz * deardia::ms2BinSize);

					binSpectra[t][iMz] += intensity;
				}
			}

			std::vector<int> sliderMS2Mz;
			std::vector<float> sliderMS2Intensity;
			std::vector< std::vector<float> > sliderMS2XICs;

			for (auto it = candidateMS2.begin(); it != candidateMS2.end(); ++it)
			{
				int nonZeroCount = 0;
				float maxValue = 0.0f;
				float nonZeroMinValue = 1e30f;
				std::vector<float> curve(deardia::sliderWidth, 0.0f);

				for (int t = 0; t < deardia::sliderWidth; ++t)
				{
					if (binSpectra[t].size() == 0) continue;
					if (*it >= binSpectra[t].size()) continue;

					if (binSpectra[t][*it] > 1.0f)
					{
						++nonZeroCount;
						curve[t] = binSpectra[t][*it];

						if (curve[t] > maxValue)
						{
							maxValue = curve[t];
						}
						if (curve[t] < nonZeroMinValue)
						{
							nonZeroMinValue = curve[t];
						}
					}
				}

				if (nonZeroCount < 5) continue;
				if (maxValue / nonZeroMinValue < 2.0f) continue;

				std::vector<float> scaleCurve(deardia::sliderWidth);
				auto m = std::minmax_element(curve.begin(), curve.end());
				float scale = *m.second - *m.first;

				for (int j = 0; j < deardia::sliderWidth; ++j)
				{
					scaleCurve[j] = (curve[j] - *m.first) / scale;
				}

				bool accept = false;
				for (int j = 0; j < sliderMS1XICs.size(); ++j)
				{
					//float p = deardia::PearsonCorrcoef(scaleCurve, sliderMS1XICs[j]);
					float p = deardia::ConcordanceCorrcoef(scaleCurve, sliderMS1XICs[j]);
					if (p > 0.2f)
					{
						accept = true;
						break;
					}
				}

				if (accept)
				{
					sliderMS2Mz.push_back(*it);
					sliderMS2XICs.push_back(scaleCurve);
					sliderMS2Intensity.push_back(*m.second);
				}
			}

			if (sliderMS2Mz.size() > deardia::minHitNumber * deardia::kClusters)
			{
				int sliderwinId = spectraData.winId;
				int sliderId = spectraData.sliderId;
				float sliderRetentionTime = spectraData.retentionTime;

				std::vector< std::vector<float> > sliderMS2XICFeatures;
				sliderMS2XICFeatures.resize(sliderMS2Mz.size(), std::vector<float>(deardia::latentDim + 1, 0.0f));

				AutoEncoder vae;
				vae.forward(sliderMS2XICs, sliderMS2XICFeatures);

				tbb::concurrent_vector<deardia::MgfData> spectraList;

				deardia::GetTandemSpectrumData(
					sliderwinId,
					sliderId,
					sliderRetentionTime,
					sliderMS1Key,
					sliderMS1ApexMz,
					sliderMS1ApexCharge,
					sliderMS2Mz,
					sliderMS2Intensity,
					sliderMS2XICs,
					sliderMS2XICFeatures,
					binSpectra,
					spectraList);

				for (int j = 0; j < spectraList.size(); ++j)
				{
					tandemSpectra.push_back(spectraList[j]);
				}
			}
		}
	}
}

float deardia::ConcordanceCorrcoef(std::vector<float>& x, std::vector<float>& y)
{
	float x_miu = 0.0f;
	float y_miu = 0.0f;
	for (int i = 0; i < deardia::sliderWidth; ++i)
	{
		x_miu += x[i];
		y_miu += y[i];
	}
	x_miu /= (float)deardia::sliderWidth;
	y_miu /= (float)deardia::sliderWidth;

	float cov_xy = 0.0f;
	float mse_xy = 0.0f;
	for (int i = 0; i < deardia::sliderWidth; ++i)
	{
		mse_xy += (x[i] - y[i]) * (x[i] - y[i]);
		cov_xy += (x[i] - x_miu) * (y[i] - y_miu);
	}

	return 1.0f / (1.0f + mse_xy / (2.0f * cov_xy));
}

void PeakFinder::findpeaksx(std::vector<float>& x, std::vector<float>& y, std::vector<int>& peakIndex)
{
	float widthPoints = 3.0;
	float slopeThreshold = 0.7 / (widthPoints * widthPoints);
	float ampThreshold = 0.1;
	float snrThreshold = 2.0;

	int peakGroup = 5;
	int smoothType = 3;
	int smoothWidth = 3;

	std::vector<float> derivSignal;
	PeakFinder::deriv(y, derivSignal);

	std::vector<float> smoothSignal;
	PeakFinder::fastsmooth(derivSignal, smoothSignal, smoothWidth, smoothType);

	int n = (int)(round(peakGroup / 2.0) + 1);

	int st = (int)(2 * round(smoothWidth / 2.0)) - 2;
	int ed = x.size() - smoothWidth - 1;

	for (int j = st; j < ed; ++j)
	{
		if (PeakFinder::sign(smoothSignal[j]) > PeakFinder::sign(smoothSignal[j + 1]))
		{
			if ((smoothSignal[j] - smoothSignal[j + 1]) > slopeThreshold)
			{
				std::vector<int> groupIndex(peakGroup);

				for (int k = 0; k < peakGroup; ++k)
				{
					groupIndex[k] = j + k - n + 1;

					if (groupIndex[k] < 0) groupIndex[k] = 0;
					if (groupIndex[k] >= y.size()) groupIndex[k] = y.size() - 1;
				}

				int apexIndex = -1;
				float maxValue = -1.0;
				for (int k = 0; k < peakGroup; ++k)
				{
					if ((y[groupIndex[k]] > maxValue) && (y[groupIndex[k]] > ampThreshold))
					{
						apexIndex = groupIndex[k];
						maxValue = y[groupIndex[k]];
					}
				}
				if (apexIndex != -1)
				{
					//peakIndex.push_back(apexIndex);

					int localMaxIndex = apexIndex;
					float localMaxValue = -1.0f;
					float localNonZeroMinValue = FLT_MAX;

					for (int k = apexIndex - 5; k <= apexIndex + 5; ++k)
					{
						if (k < 0) continue;
						if (k >= x.size()) continue;

						if (y[k] > localMaxValue)
						{
							localMaxIndex = k;
							localMaxValue = y[k];
						}
					}

					for (int k = apexIndex - 50; k <= apexIndex + 50; ++k)
					{
						if (k < 0) continue;
						if (k >= x.size()) continue;

						if ((y[k] < localNonZeroMinValue) && (y[k] > 0.1f))
						{
							localNonZeroMinValue = y[k];
						}
					}

					if (localMaxValue / localNonZeroMinValue > snrThreshold)
					{
						peakIndex.push_back(localMaxIndex);
					}
				}
			}
		}
	}

}


