bool deardia::Recalibration(std::vector<deardia::Calibrant>& calibrantList)
{
	tbb::parallel_sort(calibrantList.begin(), calibrantList.end(),
		[](deardia::Calibrant& x, deardia::Calibrant& y)
		{
			return x.score > y.score;
		});

	double D = 0.0;
	double T = 0.0;
	std::vector<double> qvalue;

	for (int i = 0; i < calibrantList.size(); ++i)
	{
		int ppid = calibrantList[i].ppid;
		if (deardia::ppidMapToPeptideInfo[ppid].isDecoy)
		{
			D += 1.0;
		}
		else
		{
			T += 1.0;
		}
		qvalue.push_back(D / T);
	}

	for (int i = qvalue.size() - 1; i >= 1; --i)
	{
		if ((calibrantList[i - 1].score == calibrantList[i].score) ||
			(qvalue[i - 1] > qvalue[i]))
		{
			qvalue[i - 1] = qvalue[i];
		}
	}

	std::vector< std::vector<float> > filterCalibrants;
	for (int i = 0; i < qvalue.size(); ++i)
	{
		if (qvalue[i] > 0.01) break;
		int ppid = calibrantList[i].ppid;
		if (deardia::ppidMapToPeptideInfo[ppid].isDecoy) continue;

		float z = (float)calibrantList[i].charge;

		float pepMass = deardia::ppidMapToPeptideInfo[ppid].pepMass;
		float theoMz = pepMass / z + 1.007276466879f;

		float expMz = calibrantList[i].mz;

		std::vector<float> tmp = { theoMz, expMz };

		filterCalibrants.push_back(tmp);
	}

	tbb::parallel_sort(filterCalibrants.begin(), filterCalibrants.end(),
		[&](std::vector<float>& x, std::vector<float>& y)
		{
			return x[0] < y[0];
		});

	std::vector<double> X;
	std::vector<double> Y;
	float prevMz = -1.0f;

	for (int i = 0; i < filterCalibrants.size(); ++i)
	{
		if (filterCalibrants[i][0] - prevMz < 1e-7)
		{
			continue;
		}
		else
		{
			Y.push_back(filterCalibrants[i][0]);
			X.push_back(filterCalibrants[i][1]);

			prevMz = filterCalibrants[i][0];
		}
	}

	if (X.size() > 500)
	{
		if (deardia::instrument == "TOF")
		{
			for (int i = 0; i < X.size(); ++i)
			{
				X[i] = sqrt(X[i]);
			}

			deardia::PolyFit(X, Y, 2);
		}
		if (deardia::instrument == "Orbitrap")
		{
			for (int i = 0; i < X.size(); ++i)
			{
				X[i] = sqrt(X[i]);
				Y[i] = 1.0 / sqrt(Y[i]);
			}

			deardia::PolyFit(X, Y, 1);
		}
		return true;
	}
	else
	{
		deardia::paramA = 0.0;
		deardia::paramB = 0.0;
		deardia::paramC = 1.0;

		return false;
	}
}

double LogCombination(int n, int m)
{
	if (m > n)
	{
		return 0;

	}
	if (m < n / 2.0)
	{
		m = n - m;
	}

	double s1 = 0;
	for (int i = m + 1; i <= n; i++)
	{
		s1 += log((double)i);
	}

	double s2 = 0;
	int ub = n - m;
	for (int i = 2; i <= ub; i++)
	{
		s2 += log((double)i);
	}

	return s1 - s2;
}

