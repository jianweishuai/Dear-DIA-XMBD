bool GaussianSmooth(std::vector< std::vector<float> >& signal, float width, int cycles)
{
	int window = int(width * signal[0].size() / (signal[0].back() - signal[0][0]));

	window = std::min(window, (int)signal[0].size());

	if (window < 3)
	{
		return false;
	}
	if (window % 2 == 0)
	{
		window -= 1;
	}

	int size = signal[0].size();
	int del = (window - 1) / 2;

	float sum = 0.0f;
	std::vector<float> w(window, 0.0);
	for (int i = 0; i < window; ++i)
	{
		float r = i - (window - 1.0) / 2.0f;
		w[i] = expf(-((r * r) / powf(window / 4.0f, 2.0f)));

		sum += w[i];
	}
	for (int i = 0; i < window; ++i)
	{
		w[i] /= sum;
	}

	std::vector<float> yAxis = signal[1];

	while (cycles != 0)
	{
		std::vector<float> s;
		for (int i = window - 1; i >= 1; --i)
		{
			s.push_back(yAxis[i]);
		}
		s.insert(s.end(), yAxis.begin(), yAxis.end());
		for (int i = size - 2; i >= size - window; --i)
		{
			s.push_back(yAxis[i]);
		}

		std::vector<float> same;
		std::vector<float> y(s.size() + window - 1, 0.0f);

		for (int i = 0; i < s.size(); ++i)
		{
			for (int j = 0; j < window; ++j)
			{
				y[i + j] += w[j] * s[i];
			}
		}

		same.assign(y.begin() + del, y.end() - del);

		yAxis.assign(same.begin() + window - 1, same.end() - window + 1);

		cycles -= 1;
	}

	signal[1] = yAxis;
}

double HypergeometricDistribution(int N, int K, int N1, int K1)
{
	double LogC_N_N1 = LogCombination(N, N1);
	double LogC_K_K1 = LogCombination(K, K1);
	double LogC_dN_dK = LogCombination(N - K, N1 - K1);

	double LogP = LogC_K_K1 + LogC_dN_dK - LogC_N_N1;

	return LogP;
}

std::vector<float> PeakFinder::SA(std::vector<float>& signal, int smoothWidth)
{
	float sumPoints = 0.0f;
	for (int i = 0; i < smoothWidth; ++i)
	{
		sumPoints += signal[i];
	}

	int length = signal.size();
	int halfWidth = (int)(round(smoothWidth / 2.0f));
	std::vector<float> smoothSignal(signal.size(), 0.0f);

	for (int i = 0; i < length - smoothWidth; ++i)
	{
		smoothSignal[i + halfWidth - 1] = sumPoints;
		sumPoints -= signal[i];
		sumPoints += signal[i + smoothWidth];
	}

	for (int i = length - smoothWidth; i < length; ++i)
	{
		smoothSignal[length - smoothWidth - 1 + halfWidth] += signal[i];
	}

	for (int i = 0; i < length; ++i)
	{
		smoothSignal[i] /= (float)smoothWidth;
	}

	return smoothSignal;
}



float deardia::PearsonCorrcoef(std::vector<float>& X, std::vector<float>& Y)
{
	float n = (float)X.size();

	float xy_sum = std::inner_product(X.begin(), X.end(), Y.begin(), 0.0f);
	float x2_sum = std::inner_product(X.begin(), X.end(), X.begin(), 0.0f);
	float y2_sum = std::inner_product(Y.begin(), Y.end(), Y.begin(), 0.0f);
	float x_sum = std::accumulate(X.begin(), X.end(), 0.0f);
	float y_sum = std::accumulate(Y.begin(), Y.end(), 0.0f);

	float varX_varY = sqrtf((x2_sum - x_sum * x_sum / n) * (y2_sum - y_sum * y_sum / n));

	return (xy_sum - x_sum * y_sum / n) / varX_varY;

}

void PeakFinder::fastsmooth(std::vector<float>& signal,
	std::vector<float>& smoothSignal, int smoothWidth, int smoothType)
{
	if (smoothType == 1)
	{
		smoothSignal = SA(signal, smoothWidth);
	}

	if (smoothType == 2)
	{
		std::vector<float> tmpSignal = SA(signal, smoothWidth);
		smoothSignal = SA(tmpSignal, smoothWidth);
	}

	if (smoothType == 3)
	{
		std::vector<float> tmpSignal1 = SA(signal, smoothWidth);
		std::vector<float> tmpSignal2 = SA(tmpSignal1, smoothWidth);
		smoothSignal = SA(tmpSignal2, smoothWidth);
	}
}

